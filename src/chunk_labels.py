def infer_chunk_label(section_title: str, text: str) -> str:
    """섹션 제목과 청크 본문으로 시각화용 라벨을 추정한다."""
    source = f"{section_title}\n{text}".lower()
    for label, keywords in _LABEL_RULES:
        for keyword in keywords:
            if keyword.lower() in source:
                return label
    return "기타"
